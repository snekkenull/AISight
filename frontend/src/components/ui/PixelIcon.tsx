/**
 * PixelIcon Component
 * 
 * Renders pixelated icons using SVG with crisp pixel edges.
 * Uses currentColor by default for terminal theme compatibility.
 */
export function PixelIcon({
  name,
  size = PIXEL_GRID_SIZE,
  color = 'currentColor',
  className = '',
  'aria-label': ariaLabel,
  'data-testid': testId,
}: PixelIconProps): JSX.Element {
  const pixels = ICON_PIXELS[name];
  
  // Calculate scale factor for the icon
  const scale = size / PIXEL_GRID_SIZE;

  return (
    <svg
      width={size}
      height={size}
      viewBox={`0 0 ${PIXEL_GRID_SIZE} ${PIXEL_GRID_SIZE}`}
      fill={color}
      className={`pixel-icon pixel-icon-${name} ${className}`}
      aria-label={ariaLabel || name.replace(/-/g, ' ')}
      role="img"
      data-testid={testId}
      style={{
        // Requirements: 12.2, 12.5 - Prevent smoothing, maintain crisp edges
        imageRendering: 'pixelated',
        // Fallback for browsers that don't support 'pixelated'
        // @ts-expect-error - crisp-edges is valid but not in TS types
        imageRendering: 'crisp-edges',
        // Ensure no anti-aliasing on the SVG
        shapeRendering: 'crispEdges',
        // Prevent any transforms from causing blur
        transform: `scale(${scale})`,
        transformOrigin: 'top left',
        width: PIXEL_GRID_SIZE,
        height: PIXEL_GRID_SIZE,
      }}
    >
      {/* Render each pixel as a 1x1 rect */}
      {pixels.map(([x, y], index) => (
        <rect
          key={`${x}-${y}-${index}`}
          x={x}
          y={y}
          width={1}
          height={1}
          fill="inherit"
        />
      ))}
    </svg>
  );
}

/**
 * Get all available icon names
 */
export function getAvailableIconNames(): PixelIconName[] {
  return Object.keys(ICON_PIXELS) as PixelIconName[];
}

/**
 * Check if an icon name is valid
 */
export function isValidIconName(name: string): name is PixelIconName {
  return name in ICON_PIXELS;
}

export default PixelIcon;
